#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	src_length;

	if (dst == NULL || src == NULL)
		return (0);
	i = 0;
	while (src[i] != '\0' && i + 1 < size)
	{
		dst[i] = src[i];
		i++;
	}
	if (size > 0)
		dst[i] = '\0';
	src_length = 0;
	while (src[src_length] != '\0')
		src_length++;
	return (src_length);
}
